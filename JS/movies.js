export class Movies{
    constructor(id, url_image, title, genre, date, rated, score, director, actors, duration, country, box_office_result, resume){
        this.id = id;
        this.url_image = url_image;
        this.title = title;
        this.genre = genre;
        this.date = date;
        this.rated = rated;
        this.score = score;
        this.director = director;
        this.actors = actors;
        this.duration = duration;
        this.country = country;
        this.box_office_result = box_office_result;
        this.resume = resume;
    }
}
